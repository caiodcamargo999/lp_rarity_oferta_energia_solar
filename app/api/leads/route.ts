import { NextRequest, NextResponse } from 'next/server'

interface LeadData {
  name: string
  whatsapp: string
  email: string
  painPoint: string
  scheduledTime: string
}

export async function POST(request: NextRequest) {
  try {
    const leadData: LeadData = await request.json()

    // Valida√ß√£o b√°sica
    if (!leadData.name || !leadData.email || !leadData.whatsapp || !leadData.painPoint || !leadData.scheduledTime) {
      return NextResponse.json(
        { error: 'Todos os campos s√£o obrigat√≥rios' },
        { status: 400 }
      )
    }

    // Valida√ß√£o de email
    const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/
    if (!emailRegex.test(leadData.email)) {
      return NextResponse.json(
        { error: 'Email inv√°lido' },
        { status: 400 }
      )
    }

    // Valida√ß√£o de WhatsApp (mais flex√≠vel)
    const cleanWhatsApp = leadData.whatsapp.replace(/\D/g, '')
    if (cleanWhatsApp.length < 10) {
      return NextResponse.json(
        { error: 'WhatsApp deve ter pelo menos 10 d√≠gitos' },
        { status: 400 }
      )
    }

    console.log('‚úÖ Lead processado com sucesso:', leadData.name)
    console.log('üìß Email:', leadData.email)
    console.log('üì± WhatsApp:', leadData.whatsapp)
    console.log('üéØ Dor principal:', leadData.painPoint)
    console.log('üìÖ Hor√°rio agendado:', leadData.scheduledTime)

    // Simular processamento bem-sucedido
    await new Promise(resolve => setTimeout(resolve, 1000))

    return NextResponse.json({
      success: true,
      message: 'Lead capturado com sucesso! Sess√£o ser√° agendada em breve.',
      leadId: `lead-${Date.now()}`,
      scheduledTime: leadData.scheduledTime
    })

  } catch (error) {
    console.error('‚ùå Erro ao processar lead:', error)
    
    return NextResponse.json(
      { 
        error: 'Erro interno do servidor',
        details: error instanceof Error ? error.message : 'Erro desconhecido'
      },
      { status: 500 }
    )
  }
}
