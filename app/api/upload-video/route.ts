import { NextRequest, NextResponse } from 'next/server';
import { uploadToBlob, isBlobConfigured } from '../../../lib/vercel-blob';

export async function POST(request: NextRequest): Promise<NextResponse> {
  try {
    // Verificar se o Vercel Blob est√° configurado
    if (!isBlobConfigured()) {
      return NextResponse.json(
        { error: 'Vercel Blob n√£o est√° configurado. Configure as vari√°veis de ambiente.' },
        { status: 500 }
      );
    }

    const formData = await request.formData();
    const file = formData.get('file') as File;
    
    if (!file) {
      return NextResponse.json(
        { error: 'Nenhum arquivo enviado' },
        { status: 400 }
      );
    }

    // Verificar se o arquivo √© um v√≠deo
    if (!file.type.startsWith('video/')) {
      return NextResponse.json(
        { error: 'Arquivo deve ser um v√≠deo' },
        { status: 400 }
      );
    }

    // Verificar tamanho do arquivo (m√°ximo 500MB)
    const maxSize = 500 * 1024 * 1024; // 500MB
    if (file.size > maxSize) {
      return NextResponse.json(
        { error: 'Arquivo muito grande. M√°ximo: 500MB' },
        { status: 400 }
      );
    }

    console.log('üìÅ Iniciando upload:', {
      name: file.name,
      size: file.size,
      type: file.type
    });

    // Upload para Vercel Blob
    const blob = await uploadToBlob(file, file.name);

    console.log('‚úÖ Upload conclu√≠do:', blob.url);

    return NextResponse.json({
      url: blob.url,
      success: true,
      message: 'V√≠deo enviado com sucesso!'
    });

  } catch (error) {
    console.error('‚ùå Erro no upload:', error);
    
    // Tratamento espec√≠fico de erros
    if (error instanceof Error) {
      if (error.message.includes('BLOB_ACCESS_DENIED')) {
        return NextResponse.json(
          { error: 'Acesso negado ao Vercel Blob. Verifique as configura√ß√µes.' },
          { status: 403 }
        );
      }
      
      if (error.message.includes('BLOB_STORE_NOT_FOUND')) {
        return NextResponse.json(
          { error: 'Store do Vercel Blob n√£o encontrado. Configure primeiro.' },
          { status: 404 }
        );
      }
    }

    return NextResponse.json(
      { error: 'Erro interno do servidor. Tente novamente.' },
      { status: 500 }
    );
  }
}
