import { NextRequest, NextResponse } from 'next/server'

export async function GET(request: NextRequest) {
  try {
    // ===== DEBUG: VERIFICAR VARI√ÅVEIS DE AMBIENTE =====
    console.log('üîç Vari√°veis de ambiente:', {
      NEXT_PUBLIC_VIDEO_URL: process.env.NEXT_PUBLIC_VIDEO_URL,
      NODE_ENV: process.env.NODE_ENV,
      VERCEL: process.env.VERCEL,
      VERCEL_ENV: process.env.VERCEL_ENV
    })
    
    // ===== URL DO V√çDEO - PRIORIDADE PARA VARI√ÅVEL DE AMBIENTE =====
    const videoUrl = process.env.NEXT_PUBLIC_VIDEO_URL || 'https://n5c9lgm3cwpfoiun.public.blob.vercel-storage.com/video-de-vendas.mp4'
    
    console.log('üé¨ Tentando carregar v√≠deo de:', videoUrl)
    
    // ===== OTIMIZA√á√ïES DE PERFORMANCE =====
    const range = request.headers.get('range')
    const start = range ? parseInt(range.replace('bytes=', '').split('-')[0]) : 0
    
    // Buscar o v√≠deo do Vercel Storage com range para streaming
    const response = await fetch(videoUrl, {
      headers: {
        'Range': range || 'bytes=0-',
        'Cache-Control': 'public, max-age=31536000, immutable',
      },
      // ===== CONFIGURA√á√ïES DE PERFORMANCE =====
      cache: 'force-cache',
      next: { revalidate: 31536000 }, // Cache por 1 ano
    })

    if (!response.ok) {
      console.error(`‚ùå Erro ao buscar v√≠deo: ${response.status} - ${response.statusText}`)
      throw new Error(`Failed to fetch video: ${response.status} - ${response.statusText}`)
    }

    console.log('‚úÖ V√≠deo carregado com sucesso! Status:', response.status)

    // Extrair headers importantes
    const contentLength = response.headers.get('content-length')
    const contentType = response.headers.get('content-type') || 'video/mp4'
    const acceptRanges = response.headers.get('accept-ranges') || 'bytes'
    const contentRange = response.headers.get('content-range')

    // ===== HEADERS OTIMIZADOS PARA PERFORMANCE =====
    const videoResponse = new NextResponse(response.body, {
      status: response.status,
      headers: {
        'Content-Type': contentType,
        'Content-Length': contentLength || '',
        'Accept-Ranges': acceptRanges,
        // ===== CACHE AGGRESSIVO =====
        'Cache-Control': 'public, max-age=31536000, immutable, s-maxage=31536000',
        'ETag': `"video-${start}-${Date.now()}"`,
        'Last-Modified': new Date().toUTCString(),
        // ===== CORS OTIMIZADO =====
        'Access-Control-Allow-Origin': '*',
        'Access-Control-Allow-Methods': 'GET, HEAD, OPTIONS',
        'Access-Control-Allow-Headers': 'Range',
        'Access-Control-Expose-Headers': 'Content-Length, Content-Range, Accept-Ranges',
        // ===== HEADERS DE V√çDEO =====
        ...(contentRange && { 'Content-Range': contentRange }),
        // ===== COMPRESS√ÉO =====
        'Vary': 'Accept-Encoding',
      },
    })

    return videoResponse

  } catch (error) {
    console.error('‚ùå Erro cr√≠tico no proxy de v√≠deo:', error)
    
    // ===== FALLBACK: TENTAR URL ALTERNATIVA =====
    try {
      console.log('üîÑ Tentando URL alternativa...')
      const fallbackUrl = 'https://n5c9lgm3cwpfoiun.public.blob.vercel-storage.com/video-de-vendas.mp4'
      
      const fallbackResponse = await fetch(fallbackUrl, {
        headers: { 'Range': request.headers.get('range') || 'bytes=0-' }
      })
      
      if (fallbackResponse.ok) {
        console.log('‚úÖ Fallback funcionou!')
        return new NextResponse(fallbackResponse.body, {
          status: fallbackResponse.status,
          headers: {
            'Content-Type': 'video/mp4',
            'Content-Length': fallbackResponse.headers.get('content-length') || '',
            'Accept-Ranges': 'bytes',
            'Cache-Control': 'public, max-age=31536000, immutable',
            'Access-Control-Allow-Origin': '*',
          },
        })
      }
    } catch (fallbackError) {
      console.error('‚ùå Fallback tamb√©m falhou:', fallbackError)
    }
    
    return NextResponse.json(
      { 
        error: 'Erro ao carregar v√≠deo',
        details: error instanceof Error ? error.message : 'Erro desconhecido',
        timestamp: new Date().toISOString()
      },
      { status: 500 }
    )
  }
}

export async function HEAD(request: NextRequest) {
  try {
    const videoUrl = process.env.NEXT_PUBLIC_VIDEO_URL || 'https://n5c9lgm3cwpfoiun.public.blob.vercel-storage.com/video-de-vendas.mp4'
    
    // Buscar apenas headers do v√≠deo
    const response = await fetch(videoUrl, { method: 'HEAD' })

    if (!response.ok) {
      throw new Error(`Failed to fetch video headers: ${response.status}`)
    }

    return new NextResponse(null, {
      status: 200,
      headers: {
        'Content-Type': 'video/mp4',
        'Content-Length': response.headers.get('content-length') || '',
        'Accept-Ranges': 'bytes',
        'Cache-Control': 'public, max-age=31536000, immutable',
        'Access-Control-Allow-Origin': '*',
        'Access-Control-Allow-Methods': 'GET, HEAD, OPTIONS',
        'Access-Control-Allow-Headers': 'Range',
      },
    })

  } catch (error) {
    console.error('Erro no HEAD do v√≠deo:', error)
    return NextResponse.json(
      { error: 'Erro ao carregar headers do v√≠deo' },
      { status: 500 }
    )
  }
}

export async function OPTIONS() {
  return new NextResponse(null, {
    status: 200,
    headers: {
      'Access-Control-Allow-Origin': '*',
      'Access-Control-Allow-Methods': 'GET, HEAD, OPTIONS',
      'Access-Control-Allow-Headers': 'Range',
      'Access-Control-Max-Age': '86400',
    },
  })
}
