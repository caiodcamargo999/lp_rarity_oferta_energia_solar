// Cache para hor√°rios dispon√≠veis (30 minutos para performance m√°xima)
const timeSlotsCache = new Map<string, { slots: string[], timestamp: number }>()
const CACHE_DURATION = 30 * 60 * 1000 // 30 minutos

export async function getAvailableTimeSlots(date: string): Promise<string[]> {
  try {
    // Verificar cache primeiro (cache mais agressivo para performance)
    const cached = timeSlotsCache.get(date)
    if (cached && (Date.now() - cached.timestamp) < CACHE_DURATION) {
      console.log(`‚úÖ Hor√°rios do cache para ${date}:`, cached.slots)
      return cached.slots
    }

    // Obter hor√°rio atual em Bras√≠lia CORRETAMENTE
    const now = new Date()
    // Bras√≠lia √© UTC-3, ent√£o adicionamos 3 horas ao UTC para obter o hor√°rio local
    const brasiliaTime = new Date(now.getTime() + (3 * 60 * 60 * 1000))
    
    // Verificar se √© hoje
    const today = brasiliaTime.toISOString().split('T')[0]
    const isToday = date === today
    
    console.log(`üïê Hor√°rio atual em Bras√≠lia: ${brasiliaTime.toLocaleString('pt-BR', { timeZone: 'America/Sao_Paulo' })}`)
    console.log(`üìÖ Data solicitada: ${date}, √â hoje? ${isToday}`)
    
    // Se for hoje e j√° passou das 17h, n√£o mostrar hor√°rios
    if (isToday) {
      const currentHour = brasiliaTime.getHours()
      const currentMinute = brasiliaTime.getMinutes()
      
      if (currentHour >= 17) {
        console.log(`üïê Hor√°rio atual: ${currentHour}:${currentMinute.toString().padStart(2, '0')} - J√° passou das 17h, n√£o h√° hor√°rios dispon√≠veis para hoje`)
        timeSlotsCache.set(date, { slots: [], timestamp: Date.now() })
        return []
      }
    }
    
    // Definir hor√°rios base (9h √†s 17h) - sempre dispon√≠veis para datas futuras
    const baseSlots = ['09:00', '10:00', '11:00', '12:00', '13:00', '14:00', '15:00', '16:00', '17:00']
    
    let availableSlots = [...baseSlots]
    
    // Se for hoje, filtrar hor√°rios que j√° passaram com buffer de 2 horas
    if (isToday) {
      const currentHour = brasiliaTime.getHours()
      const currentMinute = brasiliaTime.getMinutes()
      
      // Buffer de 2 horas para garantir que o lead tenha tempo de se preparar
      const bufferHours = 2
      const cutoffHour = currentHour + bufferHours
      
      availableSlots = baseSlots.filter(slot => {
        const slotHour = parseInt(slot.split(':')[0])
        return slotHour > cutoffHour
      })
      
      console.log(`üïê Hor√°rio atual: ${currentHour}:${currentMinute.toString().padStart(2, '0')} - Hor√°rios dispon√≠veis para hoje (buffer de ${bufferHours}h): ${availableSlots.length}`)
      console.log(`üìã Hor√°rios filtrados:`, availableSlots)
    }
    
    // Salvar no cache imediatamente
    timeSlotsCache.set(date, { slots: availableSlots, timestamp: Date.now() })
    
    console.log(`‚úÖ Hor√°rios dispon√≠veis para ${date}:`, availableSlots)
    return availableSlots
    
  } catch (error) {
    console.error('‚ùå Erro ao buscar hor√°rios dispon√≠veis:', error)
    
    // Fallback instant√¢neo - sempre retornar hor√°rios padr√£o para datas futuras
    const now = new Date()
    const brasiliaTime = new Date(now.getTime() + (3 * 60 * 60 * 1000))
    const today = brasiliaTime.toISOString().split('T')[0]
    
    if (date === today) {
      const currentHour = brasiliaTime.getHours()
      if (currentHour >= 17) {
        return []
      }
    }
    
    const fallbackSlots = ['09:00', '10:00', '11:00', '12:00', '13:00', '14:00', '15:00', '16:00', '17:00']
    return fallbackSlots
  }
}

export async function createCalendarEvent(eventData: CalendarEvent): Promise<{ eventId: string; meetLink: string }> {
  try {
    const oauth2Client = await getOAuth2Client();
    const calendar = google.calendar({ version: 'v3', auth: oauth2Client });

    // Usar o email do calend√°rio do matheusdrarity@gmail.com
    const calendarId = 'matheusdrarity@gmail.com'
    
    console.log('üìÖ Tentando criar evento no calend√°rio usando OAuth2:', calendarId)
    console.log('üìù Dados do evento:', JSON.stringify(eventData, null, 2))
    
    // Criar evento no Google Calendar
    const event = {
      summary: eventData.summary,
      description: eventData.description,
      start: {
        dateTime: eventData.startTime,
        timeZone: 'America/Sao_Paulo'
      },
      end: {
        dateTime: eventData.endTime,
        timeZone: 'America/Sao_Paulo'
      },
      attendees: eventData.attendees.map(email => ({ email })),
      conferenceData: {
        createRequest: {
          requestId: `meet-${Date.now()}`,
          conferenceSolutionKey: {
            type: 'hangoutsMeet'
          }
        }
      },
      reminders: {
        useDefault: false,
        overrides: [
          { method: 'email', minutes: 24 * 60 },    // 24 horas antes
          { method: 'popup', minutes: 2 * 60 },     // 2 horas antes
          { method: 'popup', minutes: 15 }           // 15 minutos antes
        ]
      }
    }
    
    console.log('üéØ Evento a ser criado:', JSON.stringify(event, null, 2))
    
    const response = await calendar.events.insert({
      calendarId,
      requestBody: event,
      conferenceDataVersion: 1
    })
    
    const eventId = response.data.id || ''
    const meetLink = response.data.conferenceData?.entryPoints?.[0]?.uri || ''
    
    console.log('‚úÖ Evento criado no Google Calendar:', eventId)
    console.log('üîó Link do Google Meet:', meetLink)
    
    return { eventId, meetLink }
    
  } catch (error) {
    console.error('‚ùå Erro ao criar evento no Google Calendar:', error)
    
    // Se falhar no Calendar, retornar dados mock para n√£o quebrar o fluxo
    console.log('‚ö†Ô∏è Usando dados mock para continuar o fluxo')
    return { 
      eventId: `mock-${Date.now()}`, 
      meetLink: 'https://meet.google.com/mock-link' 
    }
  }
}

