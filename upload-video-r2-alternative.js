// Informa√ß√µes do arquivo
const stats = fs.statSync(VIDEO_PATH);
const fileSizeInMB = (stats.size / (1024 * 1024)).toFixed(2);

console.log('üìä Tamanho do arquivo:', fileSizeInMB, 'MB');

async function uploadToR2Alternative() {
  try {
    const { S3Client, PutObjectCommand } = require('@aws-sdk/client-s3');
    
    console.log('üîß Configurando cliente S3 alternativo...');
    
    const client = new S3Client({
      region: R2_CONFIG.region,
      endpoint: R2_CONFIG.endpoint,
      credentials: {
        accessKeyId: R2_CONFIG.accessKeyId,
        secretAccessKey: R2_CONFIG.secretAccessKey,
      },
      // ===== CONFIGURA√á√ïES ALTERNATIVAS =====
      forcePathStyle: false, // IMPORTANTE: false para endpoint alternativo
      // ===== CONFIGURA√á√ïES DE TIMEOUT =====
      requestHandler: {
        httpOptions: {
          timeout: 600000, // 10 minutos
          connectTimeout: 120000, // 2 minutos
        }
      }
    });

    const fileStream = fs.createReadStream(VIDEO_PATH);
    
    const command = new PutObjectCommand({
      Bucket: R2_CONFIG.bucketName,
      Key: 'video-de-vendas.mp4',
      Body: fileStream,
      ContentType: 'video/mp4',
      // ===== METADADOS PARA MELHOR PERFORMANCE =====
      Metadata: {
        'x-amz-meta-upload-date': new Date().toISOString(),
        'x-amz-meta-file-size': stats.size.toString(),
        'x-amz-meta-content-type': 'video/mp4'
      }
    });

    console.log('üì§ Fazendo upload com configura√ß√£o alternativa...');
    console.log('‚è±Ô∏è  Aguarde, arquivo grande sendo processado...');
    
    const result = await client.send(command);
    
    console.log('‚úÖ Upload ALTERNATIVO conclu√≠do com sucesso!');
    console.log('üîó URL p√∫blica:', `${R2_CONFIG.endpoint}/video-de-vendas.mp4`);
    console.log('üìä ETag:', result.ETag);
    
    // ===== VERIFICAR ACESSO =====
    console.log('\nüîç Verificando acesso ao arquivo...');
    try {
      const testResponse = await fetch(`${R2_CONFIG.endpoint}/video-de-vendas.mp4`, {
        method: 'HEAD'
      });
      
      if (testResponse.ok) {
        console.log('‚úÖ Arquivo acess√≠vel publicamente!');
        console.log('üéâ Upload conclu√≠do com sucesso!');
      } else {
        console.log('‚ö†Ô∏è  Arquivo n√£o acess√≠vel publicamente. Status:', testResponse.status);
        console.log('üí° Configure o bucket como p√∫blico no dashboard do Cloudflare R2');
      }
    } catch (testError) {
      console.log('‚ö†Ô∏è  N√£o foi poss√≠vel verificar acesso p√∫blico:', testError.message);
    }
    
  } catch (error) {
    console.error('‚ùå Erro no upload alternativo:', error.message);
    
    // ===== SOLU√á√ïES ALTERNATIVAS =====
    console.log('\nüîß SOLU√á√ïES ALTERNATIVAS:');
    console.log('1. Use o script principal: node upload-video-r2.js');
    console.log('2. Verifique as credenciais no dashboard do Cloudflare R2');
    console.log('3. Confirme se o bucket est√° criado e configurado');
    console.log('4. Tente fazer upload manual via dashboard (se poss√≠vel)');
    console.log('5. Use alternativa: Backblaze B2 ou Supabase Storage');
  }
}

