const fs = require('fs');
const path = require('path');

// Configura√ß√µes do Cloudflare R2 - ENDPOINT CORRIGIDO
const R2_CONFIG = {
  // ===== ENDPOINT CORRETO DO CLOUDFLARE R2 =====
  endpoint: 'https://18d46aaf79c9700a7041bb9253f0efc4.r2.cloudflarestorage.com',
  accessKeyId: '18d46aaf79c9700a7041bb9253f0efc4',
  secretAccessKey: '97ab4b36e10a7338ced2485465a01ddc81f2ab6d426925a09ab8e2ca272b228f',
  bucketName: 'vsl-solar-rarity-brasil',
  region: 'auto'
};

// Caminho do v√≠deo (ajuste para o caminho correto)
const VIDEO_PATH = path.join(process.env.USERPROFILE || process.env.HOME, 'Downloads', 'video-de-vendas.mp4');

console.log('üöÄ Iniciando upload para Cloudflare R2...');
console.log('üìÅ Caminho do v√≠deo:', VIDEO_PATH);

// Verificar se o arquivo existe
if (!fs.existsSync(VIDEO_PATH)) {
  console.error('‚ùå Arquivo n√£o encontrado!');
  console.log('üí° Verifique se o arquivo est√° em:', VIDEO_PATH);
  console.log('üí° Ou ajuste o caminho no script');
  process.exit(1);
}

// Informa√ß√µes do arquivo
const stats = fs.statSync(VIDEO_PATH);
const fileSizeInMB = (stats.size / (1024 * 1024)).toFixed(2);

console.log('üìä Tamanho do arquivo:', fileSizeInMB, 'MB');

// Fun√ß√£o de upload corrigida
async function uploadToR2() {
  try {
    const { S3Client, PutObjectCommand } = require('@aws-sdk/client-s3');
    
    console.log('üîß Configurando cliente S3...');
    console.log('üìç Endpoint:', R2_CONFIG.endpoint);
    console.log('ü™£ Bucket:', R2_CONFIG.bucketName);
    
    const client = new S3Client({
      region: R2_CONFIG.region,
      endpoint: R2_CONFIG.endpoint,
      credentials: {
        accessKeyId: R2_CONFIG.accessKeyId,
        secretAccessKey: R2_CONFIG.secretAccessKey,
      },
      // ===== CONFIGURA√á√ïES CORRIGIDAS PARA R2 =====
      forcePathStyle: true,
      // ===== CONFIGURA√á√ïES DE SSL =====
      requestHandler: {
        httpOptions: {
          timeout: 300000, // 5 minutos
          connectTimeout: 60000, // 1 minuto
        }
      }
    });

    const fileStream = fs.createReadStream(VIDEO_PATH);
    
    const command = new PutObjectCommand({
      Bucket: R2_CONFIG.bucketName,
      Key: 'video-de-vendas.mp4',
      Body: fileStream,
      ContentType: 'video/mp4',
      // ===== REMOVER ACL (N√ÉO SUPORTADO NO R2) =====
      // ACL: 'public-read' // REMOVIDO - causa erro no R2
    });

    console.log('üì§ Fazendo upload...');
    console.log('‚è±Ô∏è  Aguarde, arquivo grande sendo processado...');
    
    const result = await client.send(command);
    
    console.log('‚úÖ Upload conclu√≠do com sucesso!');
    console.log('üîó URL p√∫blica:', `${R2_CONFIG.endpoint}/${R2_CONFIG.bucketName}/video-de-vendas.mp4`);
    console.log('üìä ETag:', result.ETag);
    
    // ===== VERIFICAR SE O ARQUIVO EST√Å ACESS√çVEL =====
    console.log('\nüîç Verificando acesso ao arquivo...');
    try {
      const testResponse = await fetch(`${R2_CONFIG.endpoint}/${R2_CONFIG.bucketName}/video-de-vendas.mp4`, {
        method: 'HEAD'
      });
      
      if (testResponse.ok) {
        console.log('‚úÖ Arquivo acess√≠vel publicamente!');
      } else {
        console.log('‚ö†Ô∏è  Arquivo n√£o acess√≠vel publicamente. Status:', testResponse.status);
        console.log('üí° Configure o bucket como p√∫blico no dashboard do Cloudflare R2');
      }
    } catch (testError) {
      console.log('‚ö†Ô∏è  N√£o foi poss√≠vel verificar acesso p√∫blico:', testError.message);
    }
    
  } catch (error) {
    console.error('‚ùå Erro no upload:', error.message);
    
    // ===== DIAGN√ìSTICO DE ERROS COMUNS =====
    if (error.message.includes('SSL') || error.message.includes('handshake')) {
      console.log('\nüîß SOLU√á√ÉO PARA ERRO SSL:');
      console.log('1. Verifique se o endpoint est√° correto');
      console.log('2. Confirme se as credenciais est√£o v√°lidas');
      console.log('3. Tente usar o endpoint alternativo:');
      console.log('   https://vsl-solar-rarity-brasil.18d46aaf79c9700a7041bb9253f0efc4.r2.cloudflarestorage.com');
    } else if (error.message.includes('AccessDenied')) {
      console.log('\nüîß SOLU√á√ÉO PARA ACESSO NEGADO:');
      console.log('1. Verifique se as credenciais est√£o corretas');
      console.log('2. Confirme se o bucket existe');
      console.log('3. Verifique permiss√µes da API key');
    } else if (error.message.includes('NoSuchBucket')) {
      console.log('\nüîß SOLU√á√ÉO PARA BUCKET N√ÉO ENCONTRADO:');
      console.log('1. Verifique se o nome do bucket est√° correto');
      console.log('2. Confirme se o bucket foi criado no Cloudflare R2');
    }
  }
}

// Verificar se as depend√™ncias est√£o instaladas
try {
  require('@aws-sdk/client-s3');
  console.log('‚úÖ Depend√™ncias encontradas, iniciando upload...');
  uploadToR2();
} catch (error) {
  console.log('üì¶ Depend√™ncias n√£o encontradas. Instale primeiro:');
  console.log('npm install @aws-sdk/client-s3 @aws-sdk/lib-storage');
}
